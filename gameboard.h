class GameBoard
{
    public:

        Sprite sprite;
        Texture tex , dummy_tex;

        GameBoard() 
        {
            tex.loadFromFile("img/board/map1.png") ;
            sprite.setTexture(tex);     
            sprite.setScale(1,1);
            this->initializeFood();   
        }

        void initializeFood()
        {
             for (int i = 0; i < ROWS; i+=2) 
            {
                for (int j = 0; j < COLS; j+=2) 
                {
                    if (gameBoard[i][j] == 0) 
                    {
                        gameBoard[i][j] = 3;
                    }
                }
            }

        }

        // Function to check collision at a given position
        bool checkCollision(int x, int y) 
        {
            // Check if the position is within the boundaries of the game board
            if (x >= 0 && x < COLS && y >= 0 && y < ROWS) 
            {
                if(gameBoard[y][x] == 0 || gameBoard[y][x] == 3 || gameBoard[y][x] == 2)
                    return false;
            }
            // If the position is outside the game board, consider it a collision
            return true;
        }

        void drawFood(RenderWindow &window , Sprite &sprite)
        {   
            CircleShape food;
            food.setRadius(3);
            food.setFillColor(Color::Yellow);
            

            for (int i = 0; i < ROWS; i++) 
            {
                for (int j = 0; j < COLS; j++) 
                {
                    if (gameBoard[i][j] == 3) 
                    {
                        food.setPosition(j * CELLSIZE_X + 10, i * CELLSIZE_Y + 10);
                        //sprite.setPosition((j * CELLSIZE_X) + 3, (i * CELLSIZE_Y) + 5);
                        window.draw(food);
                    }
                }
            }
        }

         void drawPacMan(RenderWindow &window , Sprite& sprite , int x , int y)
         {
            sprite.setPosition(x * CELLSIZE_X , y * CELLSIZE_Y);
            window.draw(sprite);
        }

        void drawMaze(RenderWindow &window) 
        {
            // Draw the maze
            RectangleShape cell(Vector2f(CELLSIZE_X,CELLSIZE_Y));
            cell.setOutlineColor(Color(0,0,0,255));
            cell.setOutlineThickness(-1.0f); // Negative thickness for outline
            for (int i = 0; i < ROWS; ++i) 
            {
                for (int j = 0; j < COLS; ++j) 
                {
                    if (gameBoard[i][j] == 1) 
                    {
                        cell.setPosition(j * CELLSIZE_X, i * CELLSIZE_Y);
                        cell.setFillColor(Color::Red); // Change color as needed
                        window.draw(cell);
                    }
                }
            }
        }
};
